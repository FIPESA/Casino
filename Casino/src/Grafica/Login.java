    public void patata(){
        if(jtfUser.getText().isEmpty() || jtfPass.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Rellene todos los campos","AVISO" , JOptionPane.WARNING_MESSAGE);
           
        }else{
            try {
                usuario = User.Logindb(jtfUser.getText(), jtfPass.getText()); 
                valido = true;
                this.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "Usuario logueado con exito");
            } catch (BanException ex) {
                JOptionPane.showMessageDialog(rootPane, "Usted esta baneado","AVISO" , JOptionPane.ERROR_MESSAGE);
                valido = false;
            } catch (UserReadingException ex) {
                JOptionPane.showMessageDialog(rootPane, "El usuario no existe","AVISO" , JOptionPane.WARNING_MESSAGE);
                valido = false;
            }
            
            
            
        }
    }

