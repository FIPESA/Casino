    private void jBAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAceptarActionPerformed

        if(jtf2Apll.getText().isEmpty() || jtfApll.getText().isEmpty() || jtfEmail.getText().isEmpty() || jtfNombre.getText().isEmpty()
           || jtfPass.getText().isEmpty() || jtfRenta.getText().isEmpty() || jtfUser.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(rootPane, "Fallo al registrarse", "ERROR", WIDTH);
          
        }else {
            try {
                usuario = User.Registerdb(jtfUser.getText(), jtfPass.getText(), jtfNombre.getText(), jtfApll.getText(), jtf2Apll.getText(), jtfEmail.getText(),Double.parseDouble(jtfRenta.getText()));
                valido = true;
                this.setVisible(false);
                JOptionPane.showMessageDialog(rootPane, "Usuario registrado correctamente");
            } catch (LoginException ex) {
                JOptionPane.showMessageDialog(rootPane, "El usuario ya existe", "ERROR", WIDTH);
            }
        }
                 
             
    }//GEN-LAST:event_jBAceptarActionPerformed

